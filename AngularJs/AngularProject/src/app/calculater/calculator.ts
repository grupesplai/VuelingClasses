export class Calculator{
  
    sum(num1 : number, num2 : number){
      return num1 + num2;
    }
    sustract(num1 : number, num2 : number){
      return num1 - num2;
    }
    multiply(num1 : number, num2 : number){
      return num1 * num2;
    }
    divide(num1 : number, num2 : number){
      return num1 / num2;
    }
  }